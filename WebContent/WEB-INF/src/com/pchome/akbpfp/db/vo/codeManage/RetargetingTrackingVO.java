package com.pchome.akbpfp.db.vo.codeManage;

public class RetargetingTrackingVO {
	int page;	
	int pageSize;
	String trackingSeq;
	String trackingName;
	String paId;
	String pfpCustomerInfoId;
	String codeType;
	String trackingRangeDate;
	String verifyStatus;
	String retargetingName;
	
	
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getPageSize() {
		return pageSize;
	}
	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}
	public String getTrackingSeq() {
		return trackingSeq;
	}
	public void setTrackingSeq(String trackingSeq) {
		this.trackingSeq = trackingSeq;
	}
	public String getTrackingName() {
		return trackingName;
	}
	public void setTrackingName(String trackingName) {
		this.trackingName = trackingName;
	}
	public String getPaId() {
		return paId;
	}
	public void setPaId(String paId) {
		this.paId = paId;
	}
	public String getPfpCustomerInfoId() {
		return pfpCustomerInfoId;
	}
	public void setPfpCustomerInfoId(String pfpCustomerInfoId) {
		this.pfpCustomerInfoId = pfpCustomerInfoId;
	}
	public String getCodeType() {
		return codeType;
	}
	public void setCodeType(String codeType) {
		this.codeType = codeType;
	}
	public String getTrackingRangeDate() {
		return trackingRangeDate;
	}
	public void setTrackingRangeDate(String trackingRangeDate) {
		this.trackingRangeDate = trackingRangeDate;
	}
	public String getVerifyStatus() {
		return verifyStatus;
	}
	public void setVerifyStatus(String verifyStatus) {
		this.verifyStatus = verifyStatus;
	}
	public String getRetargetingName() {
		return retargetingName;
	}
	public void setRetargetingName(String retargetingName) {
		this.retargetingName = retargetingName;
	}	
	
}
