package com.pchome.akbpfp.db.vo.faq;

public class FaqQuestionVO {

	private String no;
	private String faqContent;
	private String faqFId;
	private String faqQId;

	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getFaqContent() {
		return faqContent;
	}

	public void setFaqContent(String faqContent) {
		this.faqContent = faqContent;
	}

	public String getFaqFId() {
		return faqFId;
	}

	public void setFaqFId(String faqFId) {
		this.faqFId = faqFId;
	}

	public String getFaqQId() {
		return faqQId;
	}

	public void setFaqQId(String faqQId) {
		this.faqQId = faqQId;
	}
}
