package com.pchome.akbpfp.db.vo.member;

public class MemberVO {
	
	private String memberId = "";
	private String memberName = "";
	private String memberSex = "";
	private String memberBirthday = "";
	private String memberTelephone = "";
	private String memberMobile = "";
	private String memberCheckMail = "";
	private String memberZip = "";
	private String memberAddress = "";
	private String auth = "";
	
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberSex() {
		return memberSex;
	}
	public void setMemberSex(String memberSex) {
		this.memberSex = memberSex;
	}
	public String getMemberBirthday() {
		return memberBirthday;
	}
	public void setMemberBirthday(String memberBirthday) {
		this.memberBirthday = memberBirthday;
	}
	public String getMemberTelephone() {
		return memberTelephone;
	}
	public void setMemberTelephone(String memberTelephone) {
		this.memberTelephone = memberTelephone;
	}
	public String getMemberMobile() {
		return memberMobile;
	}
	public void setMemberMobile(String memberMobile) {
		this.memberMobile = memberMobile;
	}
	public String getMemberCheckMail() {
		return memberCheckMail;
	}
	public void setMemberCheckMail(String memberCheckMail) {
		this.memberCheckMail = memberCheckMail;
	}
	public String getMemberZip() {
		return memberZip;
	}
	public void setMemberZip(String memberZip) {
		this.memberZip = memberZip;
	}
	public String getMemberAddress() {
		return memberAddress;
	}
	public void setMemberAddress(String memberAddress) {
		this.memberAddress = memberAddress;
	}
	public String getAuth() {
		return auth;
	}
	public void setAuth(String auth) {
		this.auth = auth;
	}
}
